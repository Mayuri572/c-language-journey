#include <stdio.h>

// This program demonstrates the use of variables in C
int main() {
    int age = 20;
    float height = 5.4;
    char grade = 'A';

    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);

    return 0;
}

// Output:
// Age: 20  
// Height: 5.4
// Grade: A
